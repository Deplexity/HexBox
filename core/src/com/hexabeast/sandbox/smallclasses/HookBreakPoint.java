package com.hexabeast.sandbox.smallclasses;

public class HookBreakPoint {
	public float x;
	public float y;
	public boolean clockwise;
	
	public HookBreakPoint()
	{
		
	}
	
	public HookBreakPoint(float x, float y, boolean clockwise)
	{
		this.x = x;
		this.y = y;
		this.clockwise = clockwise;
	}
}
